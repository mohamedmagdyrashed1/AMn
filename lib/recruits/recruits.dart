// recruits.dart
class Recruits {
  final String name;
  final String policeNumber;
  final String recruitmentDate;
  final String address;
  final String degree;
  final String nationalId;
  final String birthDate;
  final String penalities;
  final String employer;
  final String politicalInvestigationsNumber;
  final String politicalInvestigationsDate;
  final String politicalInvestigationsResult;
  final String criminalInvestigationsNumber;
  final String criminalInvestigationsDate;
  final String criminalInvestigationsResult;
  final String facts;
  final String behaviour;

  Recruits({
    required this.name,
    required this.policeNumber,
    required this.recruitmentDate,
    required this.address,
    required this.degree,
    required this.nationalId,
    required this.birthDate,
    required this.penalities,
    required this.employer,
    required this.politicalInvestigationsNumber,
    required this.politicalInvestigationsDate,
    required this.politicalInvestigationsResult,
    required this.criminalInvestigationsNumber,
    required this.criminalInvestigationsDate,
    required this.criminalInvestigationsResult,
    required this.facts,
    required this.behaviour,
  });
}
